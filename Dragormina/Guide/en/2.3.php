<p><h2>2.3- Undead Rising in Kiman Farès</h2></p>

<p>Quest : <em>The City of Gomorrhe</em><br />
Prerequisites : 2.2- The Boitata quest</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Context</span> :<br />
The bridge of Daggard have been rebuilt, you can now venturing deeper into the land of Arsinoe, direction the forest of Kiman Fares. This forest is home to several villages and provides access to Euhéméria and the Jungle of the Thebaid. </ p>

<p><span style="text-decoration: underline;">Progress</span> :</p>

<p>1- Chief Rafloc'h wants you to bring a letter to his counterpart Chief Pevnec. You must cross the Forest Kiman Fares [ars_fild01] then you head to the east. You arrive in a new part of the forest, which is part of the village itself : Kiman Fares. You can meet with Chief Pevnec and give him your letter. [ars_fild02,249,212] </ p>

<p><center><img src="../images/acces2.3.01.jpg" alt="acces2.3.01.jpg" /><br />
<img src="../images/acces2.3.02.jpg" alt="acces2.3.02.jpg" /></center></p>

<p>2- Chief Pevnec you learn that his village is attacked by undead since few weeks. He's looking for adventurers to help stop this nightmare. He advises you to visit the Shaman Kerloic. He lives in an abandoned house to the west. [ars_fild02,31,199] He speaks of an ancient city, Gomorrhe, which was destroyed by the goddess of Mount Bacchias, some centuries ago. He recommends that you begin the investigation in this necropolis. </ p>

<p><center><img src="../images/acces2.3.03.jpg" alt="acces2.3.03.jpg" /></center></p>

<p>3- Back into the forest Kiman Fares [ars_fild01] The entrance to the cemetery is located to the west. Once inside go forward, but you have two statues that guards the necropolis who refuse you the entry. Talking to them, you learn that only members of the Royal Family of Gomorrhe can enter in the Necropolis. Go back to the Shaman Kerloic. He advises you to investigate in Soknobeus village where it seems be located the descendants of Gommorhiens. </ p>

<p><center><img src="../images/acces2.3.04.jpg" alt="acces2.3.04.jpg" /></center></p>

<p>4- Head to the south of Kiman Fares. Continue south to arrive in the village of Soknobeus to meet Ataturk. [ars_fild03,213,46] You talk about a small cemetery where are burried the survivors of Gomorrhe. You will find a symbol of the Royal Family. Return to Shaman Kerloic so he can give you his blessing before entering into the cemetery. </ p>

<p><center><img src="../images/acces2.3.05.jpg" alt="acces2.3.05.jpg" /><br />
<img src="../images/acces2.3.06.jpg" alt="acces2.3.06.jpg" /></center></p>

<p>5- The hour of battle has come! You will soon realize that the king of Gomorrhe can be killed ... You'll have to kill before the four-Mage Lich for get the King to weaken. Then attack effectively permitt to defeat the Lich King. Once dead you should talk to Malemort, an adventurer who will give you few things that he has recovered in the crypt. </ p>

<p><center><img src="../images/acces2.3.08.jpg" alt="acces2.3.08.jpg" /><br />
<img src="../images/acces2.3.07.jpg" alt="acces2.3.07.jpg" /></center></p>

<p>6- Once the king put out of harm's way, go back to the Shaman Kerloic, he said the situation havn't changed ... The case would be another. Chief Pevnec will continue research, but talking to him then you'll learn that no passengers are allowed to enter the city of Arsinoe from Ishtar Gate. For help you have given him, he offers to help you make false papers. You must meet his contact, Philerme in a hostel of Euhéméria. </ p>

