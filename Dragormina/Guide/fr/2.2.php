<p><h2>2.2- La quête de Boitata</h2></p>

<p>Quête : <em>La Colère de Boitata et le Pont Daggard</em><br />
Pré-requis : 2.1- Du départ à l'arrivée à Coppalana</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Vous voilà enfin à Coppalana, un petit village au sud d'Arsinoé. Guellronoc'h vient de vous y transporter. La route s'annonce encore longue pour atteindre la capitale.</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Vous devez retrouver Daphnis votre guide à Coppalana [coppa,231,124]. Il vous indique que pour quitter la région de Coppalana, il vous faut une autorisation du Chef du Village pour traverser le Pont Daggard. Le Chef du village est le Chef Rafloc'h [coppa,235,163], il se trouve sur la place centrale. Il nous apprend que l'esprit du village, le Boitata, est en colère et qu'il a détruit le Pont Daggard, ce qui vous empêche de quitter cette région.</p>

<p><center><img src="../images/acces2.2.01.jpg" alt="acces2.2.01.jpg" /><img src="../images/acces2.2.02.jpg" alt="acces2.2.02.jpg" /></center></p>

<p>2- Il va vous falloir calmer le Boitata pour que le pont puisse être reconstruit. Le Shaman Xaviral [coppa,95,55] vous dira comment invoquer le Boitata. Vous devez justement contacter Kloiacha en empruntant un passage souterrain. [coppa,59,87]</p>

<p><center><img src="../images/acces2.2.03.jpg" alt="acces2.2.03.jpg" /><br />
<img src="../images/acces2.2.04.jpg" alt="acces2.2.04.jpg" /></center></p>

<p>3- La servante Kloiacha invoque le Boitata qu'il va falloir calmer à coup de marteau. C'est parti pour votre premier combat en Arsinoé !</p>

<p><center><img src="../images/acces2.2.05.jpg" alt="acces2.2.05.jpg" /></center></p>

<p>Une fois le combat terminé un message apparaîtra vous disant de retourner voir le chef. Comme dans la plupart des instances d'Arsinoé vous aurez deux minutes pour quitter la salle (ici emprunter le tunnel) pour valider la quête. Ne trainez pas !</p>

<p><center><img src="../images/acces2.2.06.jpg" alt="acces2.2.06.jpg" /></center></p>

<p>4- Le Chef Rafloc'h vous félicite pour votre bravoure et vous annonce que le Pont Daggard est en reconstruction. Il vous propose une nouvelle mission : Apporter une missive aux tribus de Kiman Farès pour leur indiquer que le Pont est de nouveau réparé et que leurs relations peuvent reprendre. Une autre quête vous sera proposée, celle Klerozonheg, le cuisinier du village qui compte organiser la fête du village.</p>

<p>Dirigez-vous vers le Nord-Ouest de Coppalana pour emprunter le Pont Daggard et entrer dans la forêt de Kiman Farès.</p>

