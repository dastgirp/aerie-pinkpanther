<p><h2>9.1- La Lime du Torn-Thalas</h2></p>

<p>Quête : <em>La Lime du Torn-Thalas</em><br />
Pré-requis : 2.2- La quête de Boitata</p>

<p><center><img src="../images/lunedo10.png" alt="lunedo10.png" /></center></p>

<p><span style="text-decoration: underline;">Contexte</span> :<br />
Le Nord d'Arsinoé est un vaste désert, le désert de Kargah. Depuis des siècles des colons de l'Arsinoé ont pris les terres des gobelins, les repoussant jusque dans les régions les plus désolées. La Revanche a sonnée, les troupes goblines sont en marche et sont pratiquement à la Porte Nord d'Arsinoé !</p>

<p><span style="text-decoration: underline;">Déroulement</span> :</p>

<p>1- Les troupes d'Arsinoé sont mal en point, trois légions ont été envoyées dans la campagne contre Terkish, hélas les pertes ont été très lourde. La quatrième et dernière légion d'Arsinoé est quant à elle bloquée en La Rana. Le Général Athanase [ars_fild06,57,67] se situe à la Porte d'Istar. Il recrute des mercenaires, enfin tout ce qu'il peut pour renforcer les rangs des soldats d'Arsinoé. Pour recevoir votre affection vous devez contacter le Sénéchal Lin sur la Lime du Torn-Thalas.</p>

<p><center><img src="../images/acces9.1.01.jpg" alt="acces9.1.01.jpg" /></center></p>

<p>2- Vous devez vous rendre à la Porte Nord d'Arsinoé [ars_fild09], soit en passant directement par Arsinoé, soit en passant par les Monts Bacchias (en prenant la route de l'Ouest depuis l'abbaye du Col du Feymour). Le Sénéchal Lin [ars_fild09,130,62] vous accepte, vous devez fusionner avec une garnison de la Passe d'Octavie commandée par le Commandant Paraclet.</p>

<p><center><img src="../images/acces9.1.02.jpg" alt="acces9.1.02.jpg" /></center></p>

<p>3- Le Commandant Paraclet [ars_fild08,82,62] n'a d'autre choix que d'obéir malgré le fait qu'elle n'a quasiment plus de troupes sous ses ordres pour garder la Passe d'Octavie [ars_fild08] des bandits des Monts Bacchias. Vous devez rapporter que des hommes d'Octavie sont en marche vers le front.</p>

<p><center><img src="../images/acces9.1.03.jpg" alt="acces9.1.03.jpg" /></center></p>

<p>4- Vous voilà intégré à la III Légion d'Arsinoé en tant que troupe auxilaire. Votre première mission vous sera donnée au Bastion de la lime du Torn-Thalas. Le front est situé le long du Torn-Thalas, vous allez devoir mener une ultime contre-offensive.</p>

<p><center><img src="../images/acces9.1.04.jpg" alt="acces9.1.04.jpg" /></center></p>

<p>5- L'instance de "La Lime de Torn-Thalas" vient de débuter. Votre première étape sera de prendre le P.C. gobelin en éliminant un Héros Gobelin. Ensuite rendez-vous dans le camp des prisonniers pour les rallier à votre cause en motivant le prisonnier Capeau. Ensuite vous devez prendre la route du Nord-Est pour obtenir les clés de la prison en tuant le gardien.</p>

<p><center><img src="../images/acces9.1.05.jpg" alt="acces9.1.05.jpg" /></center></p>
<p><center><img src="../images/acces9.1.06.jpg" alt="acces9.1.06.jpg" /></center></p>
<p><center><img src="../images/acces9.1.07.jpg" alt="acces9.1.07.jpg" /></center></p>

<p>6- Une fois les clés obtenues, retournez au camp des prisonniers. Mais pendant ce temps-là des renforts sont arrivées, notamment les troupes d'élites du Général Toadinne. Cette fois-ci une bataille des tranchées va débuter ! Le Général Toadinne doit mourir, une fois tombé vous devez vous dépêcher de vous rendre vers le Désert de Kargah pour libérer le village de Nèsos [ars_fild11] et posere une nouvelle défense un peu plus loin d'Arsinoé !</p>

